// Inicializamos el contador
let i = 1;

// Mientras el contador sea menor o igual a 10 (puedes ajustar el límite según tus necesidades)
while (i <= 10) {
    // Calculamos el resultado de la multiplicación
    let resultado = 7 * i;
    // Mostramos la fila de la tabla de multiplicar
    console.log(`7 x ${i} = ${resultado}`);

    // Incrementamos el contador para pasar a la siguiente fila
    i++;
}